#!/usr/bin/env python3
"""
Re-applies the MINIMAL Ironwood (NU6.3) mocks to the vendored shared Rust crates.

WHY THIS EXISTS
---------------
The shared crates (zcash_protocol, zcash_primitives, orchard, ...) are shared with
Zebra; the REAL Ironwood support in them will be implemented by others. They are NOT
the complexity this feasibility investigation measures. But the vendored copies are
regenerated by `./distclean.sh ; ./build.sh` (cargo vendor), which wipes any direct
edits. So these throwaway mocks live here as an idempotent, re-appliable script rather
than as edits to the (gitignored, regenerated) vendored tree.

Run AFTER `./build.sh` has vendored the deps, then re-run the Rust build:
    python3 ironwood-notes/crate-mocks/apply.py
    make -C src cargo-build

What it mocks (the bare minimum so zcashd's real v6/Ironwood changes can build & run):
  * zcash_protocol: a new NU6.3 NetworkUpgrade + BranchId (0x725d9e30) + LocalNetwork
    field. (Every NU6.3 tx's ZIP-244 txid/sighash is computed in this shared crate, so
    it must recognise the branch id.)
  * zcash_primitives: NU6.3 match arms + an Ironwood v6 tx header that is routed through
    the existing v5 digest machinery (the trailing Ironwood bundle bytes are ignored by
    the v5 reader). The real ZIP would commit the Ironwood bundle in the digests.

The zcashd side serializes a REAL v6 transaction and calls the digest FFI normally; the
mock lives entirely here, in the shared crates.
"""
import glob
