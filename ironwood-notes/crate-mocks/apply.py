REPO = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))


CLONES = os.path.join(REPO, "ironwood-crates")


def clone_crate(name):
    """Copy a freshly-vendored crate into ironwood-crates/ if not already present.

    The clone lives OUTSIDE depends/, so it survives `./distclean.sh`. It is wired in
    via [patch.crates-io] in the root Cargo.toml (path deps are not checksum-verified)."""
    dest = os.path.join(CLONES, name)
    if os.path.isdir(dest):
        return dest
    src = None
    for v in glob.glob(os.path.join(REPO, "depends", "*", "vendored-sources")):
        cand = os.path.join(v, name)
        if os.path.isdir(cand):
            src = cand
            break
    if src is None:
        sys.exit(f"Cannot find vendored {name}; run ./build.sh first.")
    os.makedirs(CLONES, exist_ok=True)
    shutil.copytree(src, dest)
    print(f"  cloned {name} -> {os.path.relpath(dest, REPO)}")
    return dest


def patch(path, edits):
    """edits: list of (old, new). Idempotent: skip an edit whose `new` is already present."""
    with open(path) as f:
        content = f.read()
    changed = False
    for old, new in edits:
        if new in content:
            continue  # already applied
        if old not in content:
            sys.exit(f"ANCHOR NOT FOUND in {path}:\n---\n{old}\n---\n"
                     "The vendored crate version may have changed; update this script.")
        content = content.replace(old, new, 1)
        changed = True
    if changed:
        with open(path, "w") as f:
            f.write(content)
        print(f"  patched {os.path.relpath(path, REPO)}")
    else:
        print(f"  up-to-date {os.path.relpath(path, REPO)}")


def empty_checksum(crate_dir):
    p = os.path.join(crate_dir, ".cargo-checksum.json")
    if not os.path.exists(p):
        return
    d = json.load(open(p))
    if d.get("files") != {}:
        d["files"] = {}
        json.dump(d, open(p, "w"))
        print(f"  emptied checksum {os.path.relpath(p, REPO)}")


